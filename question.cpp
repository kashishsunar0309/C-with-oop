#include <iostream>
using namespace std;
int main()
{
    int a, b, c, d, e, f;
    a = 10;
    b = 12;
    c = a + 1;
    d = b + 1;
    e = a - 1;
    f = b - 1;
    cout << "The incremented value of a = " << c << endl;
    cout << "The incremented value of b = " << d << endl;
    cout << "The decremented value of a = " << e << endl;
    cout << "The decremented value of b = " << f << endl;
    return 0;
}